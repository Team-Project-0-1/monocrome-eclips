import { spawnSync } from 'node:child_process';
import { mkdirSync, readdirSync, statSync, writeFileSync } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const rootDir = path.resolve(__dirname, '..');
const publicDir = path.join(rootDir, 'public');
const manifestPath = path.join(rootDir, 'utils', 'generatedAssetManifest.ts');

const toPosix = (value) => value.split(path.sep).join('/');

const collectPngs = (dir) => {
  const entries = readdirSync(dir, { withFileTypes: true });
  return entries.flatMap((entry) => {
    const fullPath = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      return collectPngs(fullPath);
    }
    return entry.isFile() && entry.name.toLowerCase().endsWith('.png') ? [fullPath] : [];
  });
};

const convertToWebp = (sourcePath) => {
  const targetPath = sourcePath.replace(/\.png$/i, '.webp');
  const sourceStat = statSync(sourcePath);

  try {
    const targetStat = statSync(targetPath);
    if (targetStat.mtimeMs >= sourceStat.mtimeMs) {
      return { sourcePath, targetPath, skipped: true };
    }
  } catch {
    mkdirSync(path.dirname(targetPath), { recursive: true });
  }

  const result = spawnSync(
    'ffmpeg',
    [
      '-hide_banner',
      '-loglevel',
      'error',
      '-y',
      '-i',
      sourcePath,
      '-c:v',
      'libwebp',
      '-q:v',
      '78',
      '-compression_level',
      '6',
      targetPath,
    ],
    { encoding: 'utf8' },
  );

  if (result.status !== 0) {
    throw new Error(`Failed to convert ${sourcePath}\n${result.stderr}`);
  }

  return { sourcePath, targetPath, skipped: false };
};

const pngs = collectPngs(publicDir);
const results = pngs.map(convertToWebp);
const manifestEntries = Object.fromEntries(
  results.map(({ sourcePath, targetPath }) => [
    toPosix(path.relative(publicDir, sourcePath)),
    toPosix(path.relative(publicDir, targetPath)),
  ]),
);

const manifest = `// Generated by scripts/optimize-public-assets.mjs. Do not edit by hand.
export const optimizedAssetPaths: Record<string, string> = ${JSON.stringify(manifestEntries, null, 2)};
`;

writeFileSync(manifestPath, manifest);

const sourceBytes = results.reduce((total, { sourcePath }) => total + statSync(sourcePath).size, 0);
const targetBytes = results.reduce((total, { targetPath }) => total + statSync(targetPath).size, 0);
const converted = results.filter((result) => !result.skipped).length;
const savedPercent = sourceBytes > 0 ? Math.round((1 - targetBytes / sourceBytes) * 100) : 0;

console.log(`Optimized ${results.length} PNG assets (${converted} converted, ${results.length - converted} skipped).`);
console.log(`PNG total: ${(sourceBytes / 1024 / 1024).toFixed(2)} MB`);
console.log(`WebP total: ${(targetBytes / 1024 / 1024).toFixed(2)} MB`);
console.log(`Estimated transfer reduction: ${savedPercent}%`);
